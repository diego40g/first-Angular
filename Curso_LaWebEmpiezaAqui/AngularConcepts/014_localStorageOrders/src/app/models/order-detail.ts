export class OrderDetail {
  productId?: number;
  nameProduct?: string;
  stock?: number;
  price?: number;
  total?: number;

  constructor() {
    this.productId = this.productId;
    this.nameProduct = this.nameProduct;
    this.stock = this.stock;
    this.price = this.price;
    this.total = this.total;
  }
}
